    private void BTN_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_guardarActionPerformed
        String idstr = TxtId.getText();
        String GradoAcademico = TxtAcademico.getText();
        String Apellido = TxtApellido.getText();
        String EdadStr = TxtEdad.getText();
        String Nombre = TxtNombre.getText();

        if (idstr.equals("") || GradoAcademico.equals("") || Apellido.equals("") || Nombre.equals("") || EdadStr.equals("")) {
            System.out.println("Error No hay datos");
        } else {
            Long Id = Long.parseLong(idstr);
            int EdadUsuario = Integer.parseInt(EdadStr);
            Estudiante ObjEstudiante = new Estudiante(Id, Nombre, Apellido, EdadUsuario, GradoAcademico);
            obj.InsercionDatos(ObjEstudiante);
        }
    }//GEN-LAST:event_BTN_guardarActionPerformed
